def setup():
    frameRate(100)
    size(448, 496)
    
    for i in range(28):
        for j in range(31):
            table[j][i] = m.Map(16*i+8, 16*j+8)
            if table_representation[j][i] == 1:
                table[j][i].wall = True
            elif table_representation[j][i] == 0:
                table[j][i].small_dot = True
            elif table_representation[j][i] == 8:
                table[j][i].big_dot = True
            elif table_representation[j][i] == 6:
                table[j][i].eaten = True
            
def draw():
    global pacman
    
    img = loadImage("map.jpg")
    background(img)
    
    pacman.draw_p()
    for i in range(28):
        for j in range(31):
            table[j][i].draw_dot()
    pacman.check_position(table)
    pacman.move(table)
    
def keyPressed():
    if key == CODED:
        if keyCode == UP:
            pacman.dir = PVector(0, -1)
            pacman.turn = True
        elif keyCode == DOWN:
            pacman.dir = PVector(0, 1)
            pacman.turn = True
        elif keyCode == LEFT:
            pacman.dir = PVector(-1, 0)
            pacman.turn = True
        elif keyCode == RIGHT:
            pacman.dir = PVector(1, 0)
            pacman.turn = True
